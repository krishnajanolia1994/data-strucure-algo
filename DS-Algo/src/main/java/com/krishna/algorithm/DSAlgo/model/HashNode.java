package com.krishna.algorithm.DSAlgo.model;


public class HashNode {

	private int key;
	private int value;
	private HashNode next;
	public int getKey() {
		return key;
	}
	public void setKey(int key) {
		this.key = key;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public HashNode getNext() {
		return next;
	}
	public void setNext(HashNode next) {
		this.next = next;
	}
	
  
}
