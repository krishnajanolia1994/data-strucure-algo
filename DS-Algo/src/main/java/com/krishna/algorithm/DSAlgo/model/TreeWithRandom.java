package com.krishna.algorithm.DSAlgo.model;


public class TreeWithRandom { 
	
	private int value; 
	private TreeWithRandom left;
	private TreeWithRandom right;

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public TreeWithRandom getLeft() {
		return left;
	}

	public void setLeft(TreeWithRandom left) {
		this.left = left;
	}

	public TreeWithRandom getRight() {
		return right;
	}

	public void setRight(TreeWithRandom right) {
		this.right = right;
	}

}
